package com.anningtex.sqliteall.one;

/**
 * @Author Song
 * @Desc:
 */
public class OneBean {
    private String name;
    private String age;

    public OneBean(String name, String age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public String getAge() {
        return age;
    }
}
